#include <bits/stdc++.h>
using namespace std;

int main() {
  float A, B;
  double C, D;

  cin >> A >> B;
  cin >> C >> D;

  cout << fixed << setprecision(6);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  cout << fixed << setprecision(1);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  cout << fixed << setprecision(2);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  cout << fixed << setprecision(3);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  cout << scientific << uppercase << setprecision(3);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  cout << fixed << setprecision(0);
  cout << "A = " << A << ", B = " << B << endl;
  cout << "C = " << C << ", D = " << D << endl;

  return 0;
}